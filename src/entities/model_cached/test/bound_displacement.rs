fn bound_displacement_sofia() {
    DebugSession::new().filter(LogLevel::Info).init();
    let dbg = Dbg::new("test model_cached", "bound_displacement_sofia");
    log::debug!("\n{}", dbg);
    let test_duration = TestDuration::new(&dbg, Duration::from_secs(3000));
    test_duration.run().unwrap();
    let cache_dir: PathBuf = "src/algorithm/entities/model_cached/test/sofia".into();
    let model_dir: PathBuf = "assets/model/sofia".into();
    let model_center_coord = Position::new(65.250, 0., 0.);
    let bounds = Bounds::from_array(&PHYSICAL_FRAMES, model_center_coord.x()).unwrap();
    let thread_pool = ThreadPool::new(&dbg, Some(20));
    let delta_pos = Some(model_center_coord.x());
    let mut displacement_shape = DisplacementShape::new_uninit(
        &dbg,
        model_dir.clone().join(PathBuf::from("hull.stl")),
        delta_pos,
        1000.,
    );
    displacement_shape.init().unwrap();
    let mut bound_displacement = DisplacementBoundCache::new(
        &dbg,
        Arc::new(RwLock::new(displacement_shape)),
        cache_dir.clone().join("disp_bounded"),
        0.01,
        model_center_coord.x(),
        bounds.clone(),
        thread_pool.into(),
    );
  //  bound_displacement.init().unwrap();
    bound_displacement.rebuild().unwrap();

    let (epsilon_p, epsilon_abs) = (1., 0.2);

 /*   let _shp = |i: usize| -> String {
        let i = i as i32 - 6;
        match i {
            i@(..=114) => format!("{}", i),
            i@(115..124) => format!("114.0{}", i-114),
            _ => format!("{}", i-9),
        }
    };
    let shp = |i: usize| -> String {
        format!("{} {}", _shp(i), _shp(i+1))
    };*/
 //   println!("\n6.0 0.0");
    for (i, (r, t)) in bound_displacement
        .get(6., 0.)
        .unwrap()
        .iter()
        .zip(TARGET_6000_0.iter())
        .enumerate()
    {
        let delta = (r - t).abs();
        let delta_percent = if t.abs() > 0. { 
            delta*100./t.abs()
        } else {
            0.
        };
     //   println!("{}: {:.6}, {:.6}, {:.6}, {:.6};", shp(i), r, t, delta, delta_percent);
        assert!(
            delta_percent < epsilon_p || delta < epsilon_abs,
            "\n i:{}, result: {r}\ntarget: {t}", i as i32 - 4
        );
    }
  //  println!("\n6.0 0.1");
    for (i, (r, t)) in bound_displacement
        .get(6., 0.1)
        .unwrap()
        .iter()
        .zip(TARGET_6000_01.iter())
        .enumerate()
    {
        let delta = (r - t).abs();
        let delta_percent = if t.abs() > 0. { 
            delta*100./t.abs()
        } else {
            0.
        };
    //    println!("{}: {:.6}, {:.6}, {:.6}, {:.6};", shp(i), r, t, delta, delta_percent);
        assert!(
            delta_percent < epsilon_p || delta < epsilon_abs,
            "\n i:{}, result: {r}\ntarget: {t}", i as i32 - 4
        );
    }
 //   println!("\n4.0 1.0");
    for (i, (r, t)) in bound_displacement
        .get(4., 1.)
        .unwrap()
        .iter()
        .zip(TARGET_4000_1.iter())
        .enumerate()
    {
        let delta = (r - t).abs();
        let delta_percent = if t.abs() > 0. { 
            delta*100./t.abs()
        } else {
            0.
        };
    //    println!("{}: {:.6}, {:.6}, {:.6}, {:.6};", shp(i), r, t, delta, delta_percent);
        assert!(
            delta_percent < epsilon_p || delta < epsilon_abs,
            "\n i:{}, result: {r}\ntarget: {t}", i as i32 - 4
        );
    }
  //  println!("\n3.5, 5.0");
    for (i, (r, t)) in bound_displacement
        .get(3.5, 5.)
        .unwrap()
        .iter()
        .zip(TARGET_3500_5.iter())
        .enumerate()
    {
        let delta = (r - t).abs();
        let delta_percent = if t.abs() > 0. { 
            delta*100./t.abs()
        } else {
            0.
        };
    //    println!("{}: {:.6}, {:.6}, {:.6}, {:.6};", shp(i), r, t, delta, delta_percent);
        assert!(
            delta_percent < epsilon_p || delta < epsilon_abs,
            "\n i:{}, result: {r}\ntarget: {t}", i - 4
        );
    }
    test_duration.exit();
}
